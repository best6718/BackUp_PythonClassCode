# Print the first n values of a sequence 


# Positive Interger 
n = 20
myList = []

for i in range(1,n+1):
    myList.append(i)
    
print('Sequence starting with 1: ',myList)    

# Powers of 2
n = 20
myList = []

for i in range(1,n+1):
    myList.append(2**i)
    
print('Sequence of power of 2: ',myList)    
